def map_tushare_industry(industry_name: str) -> Optional[int]:
    """Map a Tushare industry name to an industry_tree leaf node id.

    Returns None if no leaf-level mapping exists.
    """
    if not industry_name:
        return None
    return TUSHARE_TO_LEAF.get(industry_name.strip())


def get_unmapped_industries(industry_names: set) -> set:
    """Return a set of industry names that have no leaf-level mapping."""
    return {name for name in industry_names if map_tushare_industry(name) is None}
